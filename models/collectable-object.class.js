class CollectableObject extends DrawableObject {

    /**
     * This first calles function calls the constructor function of drawable object class.
     * Also this function sets collectable objects x and y position.
     * 
     * @param {number} x 
     * @param {number} y 
     */
    constructor(x, y) {
        super();
        this.x = x;
        this.y = y;
    }
}